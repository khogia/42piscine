#include <unistd.h>

void	ft_is_negative(int n)

{
	char	rozdil;

	if (n >= 0)
	{
		rozdil = 'P';
		write(1, &rozdil, 1);
	}
	else
	{
		rozdil = 'N';
		write(1, &rozdil, 1);
	}
}
