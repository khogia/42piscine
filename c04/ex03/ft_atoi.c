#include <stdio.h>

int	ft_atoi(char *str)
{
	int	i;
	int	res;
	int	rev;

	i = 0;
	res = 0;
	rev = 1;
	while (str[i] == ' ' || (str[i] >= '\t' && str[i] <= '\r'))
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			rev = rev * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	return (res * rev);
}
