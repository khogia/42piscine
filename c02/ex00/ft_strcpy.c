char	*ft_strcpy(char *dest, char *src)
{
	int	copy;

	copy = 0;
	while (src[copy] != '\0')
	{
		dest[copy] = src[copy];
		copy++;
	}
	dest[copy] = '\0';
	return (dest);
}
